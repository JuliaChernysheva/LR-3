# Задание 8. Арифметическая прогрессия
print("ЗАДАНИЕ 8: Арифметическая прогрессия")
a1 = int(input("Введите первый член прогрессии: "))
d = int(input("Введите разность прогрессии: "))
n = int(input("Введите номер искомого члена: "))
