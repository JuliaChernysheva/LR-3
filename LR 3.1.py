# Задание 6. Стоимость покупки
print("ЗАДАНИЕ 6: Стоимость покупки трех компьютеров")
monitor = int(input("Введите стоимость монитора: "))
system_block = int(input("Введите стоимость системного блока: "))
keyboard = int(input("Введите стоимость клавиатуры: "))
mouse = int(input("Введите стоимость мыши: "))
